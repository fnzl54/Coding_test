SELECT FOOD_PRODUCT.CATEGORY, FOOD_PRODUCT.PRICE, FOOD_PRODUCT.PRODUCT_NAME
FROM FOOD_PRODUCT, (SELECT CATEGORY, MAX(PRICE) as MAX_PRICE
                    FROM FOOD_PRODUCT
                    WHERE CATEGORY IN ('과자','국','김치','식용유')
                    GROUP BY CATEGORY) FOOD_PRODUCT_TEMP
WHERE FOOD_PRODUCT.CATEGORY = FOOD_PRODUCT_TEMP.CATEGORY AND FOOD_PRODUCT.PRICE = FOOD_PRODUCT_TEMP.MAX_PRICE
ORDER BY FOOD_PRODUCT.PRICE DESC